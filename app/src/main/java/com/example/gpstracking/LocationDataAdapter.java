public class LocationDataAdapter extends ArrayAdapter<LocationData> {
    private final Context context;
    private final List<LocationData> values;

    public LocationDataAdapter(Context context, List<LocationData> values) {
        super(context, android.R.layout.simple_list_item_1, values);
        this.context = context;
        this.values = values;
    }

    @NonNull
    @Override
    public View getView(int position, View convertView, @NonNull ViewGroup parent) {
        LayoutInflater inflater = (LayoutInflater) context.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
        View rowView = inflater.inflate(android.R.layout.simple_list_item_1, parent, false);
        TextView textView = rowView.findViewById(android.R.id.text1);

        LocationData locationData = values.get(position);
        textView.setText(locationData.toString());

        return rowView;
    }
}

